public class Lotnisko {
    private String nazwaLotniska;
    private float adres;

    public Lotnisko(String nazwaLotniska, float adres) {
        this.nazwaLotniska = nazwaLotniska;
        this.adres = adres;
    }

    public void setNazwaLotniska(String nazwaLotniska) {
        this.nazwaLotniska = nazwaLotniska;
    }

    public void setAdres(float adres) {
        this.adres = adres;
    }
}
